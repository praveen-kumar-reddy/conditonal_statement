package demo_pack_statement;

public class if_else_if_statement {
	public static void main(String[] args) {
		int num=10;
		if(num<10) {
			System.out.println("the number 10 print is right");
		}
		else if(num<20) {
			System.out.println("the number 20 is print is right");
		}
		else if(num>40) {
			System.out.println("the number 60 is print is right");
		}
		else {
			System.out.println("other wise its default number");
		}

}
}